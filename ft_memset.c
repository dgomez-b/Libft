/* ******************************* LIBRERIAS ******************************** */

#include <string.h>

/* ******************************* PROTOTIPOS ******************************* */

/* ******************************* FUNCIONES ******************************** */

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*i;

	i = b;
	while (len-- > 0)
		i[len] = c;
	return (i);
}
