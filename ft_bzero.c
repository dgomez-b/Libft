/* ******************************* LIBRERIAS ******************************** */

#include "libft.h"

/* ******************************* FUNCIONES ******************************** */

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*i;

	i = s;
	while (n-- > 0)
		i[n] = 0;
}
