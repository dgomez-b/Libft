#include <string.h>
#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include "libft.h"

/* ******************************* PROTOTIPOS ******************************* */

/* ******************************** FUNCIONES ******************************* */

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
