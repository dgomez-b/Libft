/* ******************************* LIBRERIAS ******************************** */

#include "libft.h"

/* ******************************* FUNCIONES ******************************** */

char	*ft_strnstr(const char *s1, char *s2, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (*s2 == '\0')
		return ((char *)s1);
	if ((len == 0) || (*s1 == '0'))
		return (0x0);
	while ((s1[i] != '\0') && (i + j < len))
	{
		j = 0;
		while ((i + j < len) && (s1[i + j] == s2[j]) && (s1[i + j]
				&& s2[j] != '\0'))
			j++;
		if (s2[j] == '\0')
			return ((char *)(s1 + i));
		i++;
	}
	return (0x0);
}
