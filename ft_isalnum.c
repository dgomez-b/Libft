/* ******************************* LIBRARIES ******************************** */

#include "./libft.h"

/* ******************************* FUNCTIONS ******************************** */

int	ft_isalnum(int c)
{
	int	i;

	i = 0;
	i += ft_isalpha(c);
	i += ft_isdigit(c);
	return (i);
}
