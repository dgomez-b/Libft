/* ******************************* LIBRERIAS ******************************** */

#include "libft.h"

/* ******************************* FUNCIONES ******************************** */

void	ft_lstclear(t_list **lst, void (*f)(void *))
{
	t_list	*cur;

	if (!lst || !f)
		return ;
	while (*lst)
	{
		cur = (*lst)->next;
		ft_lstdelone(*lst, f);
		*lst = cur;
	}
	*lst = 0;
}
