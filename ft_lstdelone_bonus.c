/* ******************************* LIBRERIAS ******************************** */

#include "libft.h"
#include <stdlib.h>

/* ******************************* FUNCIONES ******************************** */

void	ft_lstdelone(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	f(lst->content);
	free(lst);
}
