/* ********************************LIBRARIES********************************* */

#include "./libft.h"

/* ********************************FUNCTIONS********************************* */

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*ssrc;
	char	*sdst;

	if (dst == src)
		return (dst);
	ssrc = (char *)src;
	sdst = (char *)dst;
	while (n > 0)
	{
		n--;
		sdst[n] = ssrc[n];
	}
	return (dst);
}
