#include <string.h>

/* ******************************* PROTOTIPOS ******************************* */

/* ******************************* LIBRERIAS ******************************** */

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	return (i);
}
