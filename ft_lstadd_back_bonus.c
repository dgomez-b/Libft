/* ******************************* LIBRERIAS ******************************** */

#include "libft.h"

/* ******************************* FUNCIONES ******************************** */

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (lst && *lst && new)
		ft_lstlast(*lst)->next = new;
	else if (!*lst)
		*lst = new;
}
