/* ********************************LIBRARIES********************************* */

#include "./libft.h"
#include <stdlib.h>

/* ********************************FUNCTIONS********************************* */

char	*ft_strdup(const char *s)
{
	char	*rs;
	size_t	len;

	len = ft_strlen(s);
	rs = malloc(sizeof(char) * len + 1);
	if (!rs)
		return (0);
	rs[len] = 0;
	while (len-- > 0)
		rs[len] = s[len];
	return (rs);
}
