/* ******************************* LIBRERIAS ******************************** */

#include <unistd.h>
#include "libft.h"

/* ******************************* PROTOTIPOS ******************************* */

/* ******************************* FUNCIONES ******************************** */

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	c = '0';
	if (n < 0)
	{
		c = '0' + ((n % 10) * -1);
		n /= 10;
		if (write(fd, "-", 1) == -1)
			return ;
		if (n != 0)
			ft_putnbr_fd(n * -1, fd);
	}
	else if (n > 0)
	{
		c = '0' + (n % 10);
		n /= 10;
		if (n != 0)
			ft_putnbr_fd(n, fd);
	}
	if (write(fd, &c, 1) == -1)
		return ;
}
