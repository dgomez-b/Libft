/* ********************************LIBRARIES********************************* */

#include "./libft.h"

/* ********************************FUNCTIONS********************************* */

int	ft_atoi(const char *s)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	j = 1;
	k = 0;
	while ((s[i] == ' ') || (s[i] == '\t') || (s[i] == '\n') || (s[i] == '\v')
		|| (s[i] == '\f') || (s[i] == '\r'))
		i++;
	if (s[i] == '-')
		j *= -1;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		if (k * 10 + s[i] - '0' > 2147483647)
			return (0);
		k = k * 10 + s[i++] - '0';
	}
	return (k * j);
}
